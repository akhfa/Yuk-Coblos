    public FormPemilih(String NIK, String Password) {
        dataPemilih = new main.Pemilih(NIK, Password);
        boolean[] isSudahPilih = dataPemilih.LoadStatusSudahMemilihAtauBelum();
        isSudahPilihDPR = isSudahPilih[0];
        isSudahPilihDPD = isSudahPilih[1];
        isSudahPilihDPRDP = isSudahPilih[2];
        isSudahPilihDPRDK = isSudahPilih[3];
        initComponents();
        DPR.setEnabled(!isSudahPilihDPR);
        DPD.setEnabled(!isSudahPilihDPD);
        DPRDProvinsi.setEnabled(!isSudahPilihDPRDP);
        DPRDKabupaten.setEnabled(!isSudahPilihDPRDK);
        if(!isSudahPilihDPD || !isSudahPilihDPR || !isSudahPilihDPRDP || !isSudahPilihDPRDK){
            Submit.setEnabled(false);
        }
    }
    
    public main.Pemilih GetPemilih(){
        return dataPemilih;
    }

